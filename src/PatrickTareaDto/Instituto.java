package PatrickTareaDto;

public class Instituto {
    private String Ubicacion_Sede;
    private String Modalidad_Clase;
    private int Duracion_Dia;
    private String Carreras;
    private String Tipo_Titulos;
    private String Tipo_Institutos;
    
    public void setUbicacion_Sede(String Ubicacion_Sede){
        this.Ubicacion_Sede = Ubicacion_Sede;
    }
    
    public void setModalidad_Clase(String Modalidad_Clase){
        this.Modalidad_Clase = Modalidad_Clase;
    }
    
    public void setDuracion_Dia(int Duracion_Dia){
        this.Duracion_Dia = Duracion_Dia;
    }
    
    public void setCarreras(String Carreras){
        this.Carreras = Carreras;
    }
    
    public void setTipo_Titulos(String Tipo_Titulos){
        this.Tipo_Titulos = Tipo_Titulos;
    }
    
    public void setTipo_Institutos(String Tipo_Institutos){
        this.Tipo_Institutos = Tipo_Institutos;
    }
    
    public String getUbicacion_Sede(){
        return Ubicacion_Sede;
    }
    
    public String getModalidad_Clase(){
        return Modalidad_Clase;
    }
    
    public int getDuracion_Dia(){
        return Duracion_Dia;
    }
    
    public String getCarreras(){
        return Carreras;
    }
    
    public String getTipo_Titulos(){
        return Tipo_Titulos;
    }
    
    public String getTipo_Institutos(){
        return Tipo_Institutos;
    }
}

